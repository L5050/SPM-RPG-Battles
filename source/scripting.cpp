s32 npc_get_bounding_box(spm::evtmgr::EvtEntry * evtEntry, bool firstRun)
{
    spm::evtmgr::EvtVar * args = (spm::evtmgr::EvtVar *)evtEntry->pCurData;
    char * name = (char *)spm::evtmgr_cmd::evtGetValue(evtEntry, args[0]);

    Vec3 ret1 = {0, 0, 0};
    Vec3 ret2 = {0, 0, 0};

    spm::npcdrv::npcGetFrameBoundingBox(spm::npcdrv::npcNameToPtr(name), &ret1, &ret2);
    wii::os::OSReport("ret1 %f\n", ret1.x);
    wii::os::OSReport("ret1 %f\n", ret1.y);
    wii::os::OSReport("ret1 %f\n", ret1.z);
    spm::mario::marioGetScreenPos(&ret1, &ret2.x, &ret2.y, &ret2.z);
    wii::os::OSReport("ret2 %f\n", ret1.y);
    spm::evtmgr_cmd::evtSetFloat(evtEntry, args[1], ret1.x);
    spm::evtmgr_cmd::evtSetFloat(evtEntry, args[2], ret1.y);
    spm::evtmgr_cmd::evtSetFloat(evtEntry, args[3], ret2.x);
    spm::evtmgr_cmd::evtSetFloat(evtEntry, args[4], ret2.y);
    

    return 2;
}

